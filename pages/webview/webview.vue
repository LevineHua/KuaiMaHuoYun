<template>
	<view>
		<web-view :src="url" @message="getMessage"></web-view>
	</view>
</template>

<script>
	export default {
		data() {
			return {
				url: ''
			}
		},
		onLoad(options) {
			this.url = this.serverURL+'home/login/user_subscribe_code.html?uid=' + options.uid;
		},
		methods: {
			getMessage(event) {
				console.log(event.detail)
				uni.showModal({
					content: JSON.stringify(event.detail),
					showCancel: false
				});
			}
		}
	}
</script>

